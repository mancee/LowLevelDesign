package BookMyShow;


public class HallSeat {
	private int row,col;
	
	HallSeat(int r, int c){
		this.row=r;
		this.col=c;
	}

	public int getRow() {
		return row;
	}

	public void setRow(int row) {
		this.row = row;
	}

	public int getCol() {
		return col;
	}

	public void setCol(int col) {
		this.col = col;
	}
	
	
}
